// Builds src/intl-tel-input/validation.js with Closure Compiler using JSON input streams.
// This keeps the source list deterministic without relying on compiler glob
// expansion or hitting Windows command-length limits. Uses the native binary,
// no Java required.
import fs from 'node:fs';
import { createRequire } from 'node:module';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(__dirname, '..');

const require = createRequire(import.meta.url);
const Compiler = require('google-closure-compiler/lib/node/index.js').default;
const { getNativeImagePath } = require('google-closure-compiler/lib/utils.js');

// libphonenumber lives at the monorepo root as a git submodule
const repoRoot = rootDir;
const libphonenumberBase = path.join(repoRoot, 'third_party', 'libphonenumber');
const requiredLibphonenumberFile =
  path.join(libphonenumberBase, 'javascript', 'i18n', 'phonenumbers', 'phonenumberutil.js');
const closureLibraryDir = path.join(rootDir, 'node_modules', 'google-closure-library');
const libphonenumberDir = path.join(libphonenumberBase, 'javascript', 'i18n', 'phonenumbers');

const excludedLibphonenumberFiles = new Set([
  path.join(libphonenumberDir, 'demo-compiled.js'),
  path.join(libphonenumberDir, 'metadatafortesting.js'),
  path.join(libphonenumberDir, 'metadatalite.js'),
  path.join(libphonenumberDir, 'regioncodefortesting.js'),
].map(toPosixPath));

function toPosixPath(filePath) {
  return filePath.replaceAll(path.sep, '/');
}

function collectJsFiles(dirPath) {
  const files = [];
  const pending = [dirPath];

  while (pending.length) {
    const currentDir = pending.pop();
    const entries = fs.readdirSync(currentDir, { withFileTypes: true });

    for (const entry of entries) {
      const entryPath = path.join(currentDir, entry.name);

      if (entry.isDirectory()) {
        pending.push(entryPath);
      } else if (entry.isFile() && entry.name.endsWith('.js')) {
        files.push(toPosixPath(entryPath));
      }
    }
  }

  return files.sort();
}

if (!fs.existsSync(requiredLibphonenumberFile)) {
  throw new Error(`Missing required libphonenumber source: ${requiredLibphonenumberFile}. Run "git submodule update --init --recursive" first.`);
}

const entryFile = path.join(rootDir, 'src', 'intl-tel-input', 'validation.js');
const outputFile = path.join(rootDir, 'src', 'generated', 'validation.generated.ts');
const tempFile = path.join(rootDir, 'src', 'generated', 'validation.compiled.js');
const entrySource = fs.readFileSync(entryFile, 'utf8');

const compilerSources = [
  ...collectJsFiles(closureLibraryDir),
  ...collectJsFiles(libphonenumberDir).filter(
    (filePath) =>
      !excludedLibphonenumberFiles.has(filePath) && !filePath.endsWith('_test.js'),
  ),
];
const compilerInputSources = [
  ...compilerSources.map((filePath) => ({
    path: filePath,
    src: fs.readFileSync(filePath, 'utf8'),
  })),
  { path: toPosixPath(entryFile), src: entrySource },
];

const compiler = new Compiler({
  entry_point: 'goog:i18n.phonenumbers.demo',
  compilation_level: 'ADVANCED_OPTIMIZATIONS',
  js_output_file: toPosixPath(tempFile),
  output_wrapper: '%output%',
});

// Use the native binary, matching what the gulp plugin does.
compiler.JAR_PATH = null;
compiler.javaPath = getNativeImagePath();
if (!compiler.javaPath) {
  throw new Error(`No native closure-compiler binary for platform: ${process.platform}`);
}

// Stream the full source set via stdin.
compiler.commandArguments.push('--json_streams', 'IN');

const child = compiler.run();
child.stdin.write(JSON.stringify(compilerInputSources));
child.stdin.end();

let stderr = '';
child.stderr.on('data', (d) => { stderr += d; process.stderr.write(d); });
child.on('exit', (code) => {
  if (code !== 0) {
    process.stderr.write(stderr);
    process.exit(code ?? 1);
  }

  // Read the compiled output and wrap it in a TypeScript file
  const compiled = fs.readFileSync(tempFile, 'utf8');
  const tsContent = [
    '// AUTO-GENERATED by build-validation.js — do not edit.',
    '/* eslint-disable */',
    '// @ts-nocheck',
    'import type { ItiUtils } from "../intl-tel-input";',
    'const _scope: Record<string, any> = {};',
    '(function () {' + compiled + '}).call(_scope);',
    'const validation: ItiUtils = _scope["validation"];',
    'export default validation;',
    '',
  ].join('\n');

  fs.writeFileSync(outputFile, tsContent);
  fs.unlinkSync(tempFile);
  console.log(`Built ${outputFile}`);
});
